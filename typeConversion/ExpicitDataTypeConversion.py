# Explicit conversion also known as type casting is when data type conversion takes place because
# you clearly defined it in your program. You basically force an expression to be of a specific type.

# Integer and Float Conversions

# To convert integer to float, use the float() function in Python.
a_int = 3
b_int = 2

# Explicit type conversion from int to float
c_float_sum = float(a_int + b_int)
print(c_float_sum)